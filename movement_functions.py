# Button actions file

# Forward
def forward(turtle, value):
    turtle.fd(value)

# Backward
def backward(turtle, value):
    turtle.bk(value)

# Left
def left45(turtle):
    turtle.lt(45)

def left90(turtle):
    turtle.lt(90)

def left(turtle, amount):
    turtle.lt(amount)
    
# Right
def right45(turtle):
    turtle.rt(45)

def right90(turtle):
    turtle.rt(90)

def right(turtle, amount):
    turtle.rt(amount)
